# + - * / //(切り捨て演算) % **(べき乗)

x = 10
print(x / 3) # 3.33...
print(x // 3) # 3
print(x % 3) # 1
print(x ** 3) # 100

y = 4
y += 12 # y = y + 12
print(y) # 16

# and or not
print(True and False) # False
print(True or False) # True
print(not False) # True

# + *
print("hello " + "world")
print("hello " * 3)
